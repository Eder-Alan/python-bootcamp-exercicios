# - Use um loop para fazer uma contagem regressiva de 5 até 1,
# imprimindo cada número.

print('Programa que faz contagem regressiva de 5 a 1')

for x in range(5,0,-1):
    print(x)

print('------------')

var_contador = 5
while(var_contador > 0):
    print(var_contador)
    var_contador -= 1

print('------------')
for x in range(5,0,-1):
    print(x)
