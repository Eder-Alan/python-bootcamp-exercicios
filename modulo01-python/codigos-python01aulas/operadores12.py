# operadores
# operdores aritiméticos + - / *
# // trás a parte inteira de uma divisão
# % trás o resto da divisão
# x * x * x    --> x**3
# aula 04 do modulo de python, professor Ivan.
print('Soma de dois valores 5 + 2 = ',  5 + 2)
print('Subtração de dois valores 5 - 2 = ', 5 - 2)
print('Subtração de dois valores 2.25 - 5 = ', 2.25 - 5)
print('Multiplicação de dois valores 5 * 2 = ', 5 * 2)
print('Divisão de dois valores 5 / 2 = ', 5 / 2)
print('Média de dois valores (5 + 8) / 2 = ', (5 + 8) / 2)
print('Parte inteira da divisão (5 + 8) // 2 = ', (5 + 8) // 2)
print('O resto da divisão de dois valores (5 + 8) % 2 = ', (5 + 8) % 2)
print('O resto da divisão de dois valores (4 + 8) % 2 = ', (4 + 8) % 2)
print('A potencia de valores 3 * 3 * 3 = ', 3**3)
print('A potencia de valores 3 = (3 * 3 * 3) = ', 3**3)
print('A potencia de valores 4 = 4^4 = ', 4**4)
print('A potencia de valores 2^5 = (2 * 2 * 2 * 2 * 2) = ', 2**5)